void	ft_rev_int_tab(int *tab, int size)
{
	int		i;
	char	j;

	i = 0;
	size--;
	while (size > i)
	{
		j = tab[i];
		tab[i] = tab[size];
		tab[size] = j;
		size--;
		i++;
	}
}
